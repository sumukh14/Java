package Class;

public class switchcase {
    public static void main(String[] args) {
        int num = 20;
        switch (num){
            case 10:
                System.out.println("it is 10");
                break;
            case 20:
                System.out.println("it is 20");
                break;
            default:
                System.out.println("it is not present");
        }
    }
}
